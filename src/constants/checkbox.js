export function IncludedSrings(my, setError) {
  let arr = [];
  if (my.UpperCase) arr = [...arr, ...upperLetters];
  if (my.LowerCase) arr = [...arr, ...lowerLetters];
  if (my.Numbers) arr = [...arr, ...nums];
  if (my.Symbols) arr = [...arr, ...symbols];
  return arr;
}
